<div class="Help_Section">
	<p><div class="Help_Header">O que  é função padrão?</div>
	<table width="100%" class="LightShadedBox"><tr><td>Cada grupo tem uma função padrão, que é simplesmente o papel "padrão" para um membro desse grupo. Para uma classe, por exemplo, a função padrão pode ser estudante, porque 95% das pessoas da classe serão Estudantes. Haverá outros papéis - Professor, Professor Assistente, Diretor,etc -, mas a maioria de todos serão um estudante, então essa é a função padrão.
	<p>Funções padrão permite que você adicione rapidamente novos membros a um grupo. Se você tem 200 pessoas em seu carrinho de compras, você não tem que especificar Funções para cada registro para despejar o carrinho a um grupo, eles são simplesmente adicionados como função padrão. Isso também permite que a triagem rápida dos organizadores de um grupo. </td></tr></table>
</div>

